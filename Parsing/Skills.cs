    public static class SkillIdHelper
    {
        public static string GetSkillName(ushort skillId)
        {
            if (Enum.IsDefined(typeof(SkillId), skillId))
            {
                string skillName = ((SkillId)skillId).ToString();
                skillName = Regex.Replace(skillName, "(?!^)([A-Z])", " $1");
                return skillName;
            }

            return "";
        }
    }
